package com.pragra.Day6;

public class OddOrEven {

    public static void main(String[] args) {
        OddorEven(1109);
    }

    public static void OddorEven(int x) {
        if (x % 2 == 0) {
            System.out.println("Even Number");
        }
        else {
            System.out.println("Odd Number");
        }
    }
}
